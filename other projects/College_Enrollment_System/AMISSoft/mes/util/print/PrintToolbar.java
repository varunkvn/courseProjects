    public void actionPerformed(java.awt.event.ActionEvent e){
        Object o = e.getSource ();
        if (o.equals(btnNext)) {
            mainPrintFrame.previewReport.pageIndex ++;
            mainPrintFrame.previewReport.repaint();
        }else
        if (o.equals(btnPrev)) {
            mainPrintFrame.previewReport.pageIndex --;
            mainPrintFrame.previewReport.repaint();
        }else
        if (o.equals(btnFirst)) {
            mainPrintFrame.previewReport.pageIndex = 0;
            mainPrintFrame.s.getViewport().repaint();
            
        }else
        if (o.equals(btnLast)) {
            mainPrintFrame.previewReport.pageIndex = mainPrintFrame.previewReport.printCalc.totalNumPages;
            mainPrintFrame.s.getViewport().repaint();
        }else
        if (o.equals(btnPrint)){
            mainPrintFrame.print();
        }else
        if (o.equals(btnPrintProp)){
            mainPrintFrame.setUpPrinter();
        }
        
        else
        if (o.equals(btnIn)){
            mainPrintFrame.previewReport.zoomIn();
        }
        
        else
        if (o.equals(btnOut)){
            mainPrintFrame.previewReport.zoomOut();
        }
        
        else
        if (o.equals(this.btnResZoom)){
            mainPrintFrame.previewReport.restoreZoom();
        }
        
        
        else
        if (o.equals(this.btnPageProp)){
            showManualPageSetup();
        }       
    }
    
    public void showManualPageSetup(){
        if(manualPageSetup==null){
            manualPageSetup = new ManualPageSetup(){
                public void setVisible(boolean b){
                    if(!b){
                        mainPrintFrame.pageFormat = manualPageSetup.getPageFormat();
                        java.awt.Dimension d = new java.awt.Dimension((int)mainPrintFrame.pageFormat.getWidth(),(int)mainPrintFrame.pageFormat.getHeight());
                        mainPrintFrame.previewReport.setPreferredSize(d);
	                    mainPrintFrame.previewReport.repaintParent();
                        mainPrintFrame.repaint();
                    }
                    super.setVisible(b);
                }
                };
        }

        manualPageSetup.show(btnPageProp, 1, 1);
        manualPageSetup.setPageFormat(mainPrintFrame.pageFormat);
    }
    
